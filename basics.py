
# comment: use hash symbol
# shortcut: ctrl + /
# how to use double quotation under double quotation?
# use backward slash before double quoation
# example:
print("In the bleak \"Midwinter\"")
print("Hey", 7, 8)

# sep: separates each word
print("Hey", 7, 8, sep="~")
print("Hey", 7, 8, sep="&")
# print("Hey", a, b) is not correct
# correct version:
print("Hey", 7, 8, sep="&")
print("Hey a b")

# end: specify what to print at the end
print("Hey", 7, 8, sep="~",end=" like this")

